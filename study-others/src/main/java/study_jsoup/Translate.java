        String query = str;
        String salt = String.valueOf(System.currentTimeMillis());
        String from = "en";
        String to = "zh_CHS";
        String sign = md5(appKey + query + salt+ "mfc0iEiVY4xWUBFlTjxKnlUZbblN8KJ4");
        Map<String, String> params = new HashMap<String, String>();
        params.put("q", query);
